#include <bits/stdc++.h>
using namespace std;

int main() {
    int n, x;

    cin >> n >> x;

    if (0 < x && x <= n) {
        cout << "A" << endl;
    } else if (n < x && x <= 2 * n) {
        cout << "B" << endl;
    } else if (2 * n < x && x <= 3 * n) {
        cout << "C" << endl;
    } else if (3 * n < x && x <= 4 * n) {
        cout << "D" << endl;
    } else if (4 * n < x && x <= 5 * n) {
        cout << "E" << endl;
    } else if (5 * n < x && x <= 6 * n) {
        cout << "F" << endl;
    } else if (6 * n < x && x <= 7 * n) {
        cout << "G" << endl;
    } else if (7 * n < x && x <= 8 * n) {
        cout << "H" << endl;
    } else if (8 * n < x && x <= 9 * n) {
        cout << "I" << endl;
    } else if (9 * n < x && x <= 10 * n) {
        cout << "J" << endl;
    } else if (10 * n < x && x <= 11 * n) {
        cout << "K" << endl;
    } else if (11 * n < x && x <= 12 * n) {
        cout << "L" << endl;
    } else if (12 * n < x && x <= 13 * n) {
        cout << "M" << endl;
    } else if (13 * n < x && x <= 14 * n) {
        cout << "N" << endl;
    } else if (14 * n < x && x <= 15 * n) {
        cout << "O" << endl;
    } else if (15 * n < x && x <= 16 * n) {
        cout << "P" << endl;
    } else if (16 * n < x && x <= 17 * n) {
        cout << "Q" << endl;
    } else if (17 * n < x && x <= 18 * n) {
        cout << "R" << endl;
    } else if (18 * n < x && x <= 19 * n) {
        cout << "S" << endl;
    } else if (19 * n < x && x <= 20 * n) {
        cout << "T" << endl;
    } else if (20 * n < x && x <= 21 * n) {
        cout << "U" << endl;
    } else if (21 * n < x && x <= 22 * n) {
        cout << "V" << endl;
    } else if (22 * n < x && x <= 23 * n) {
        cout << "W" << endl;
    } else if (23 * n < x && x <= 24 * n) {
        cout << "X" << endl;
    } else if (24 * n < x && x <= 25 * n) {
        cout << "Y" << endl;
    } else if (25 * n < x && x <= 26 * n) {
        cout << "Z" << endl;
    }
}
